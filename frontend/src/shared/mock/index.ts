export function getTotalAssetsKrw(accounts: Account[]): number {
  return accounts.reduce((sum, account) => sum + account.currentValueKrw, 0);
}

export function getTotalPrincipalKrw(accounts: Account[]): number {
  return accounts.reduce((sum, account) => sum + account.principalKrw, 0);
}
